package io.github.microapplet.mams.user.controller;

import io.github.microapplet.mams.user.model.IdentificationUser;
import io.github.microapplet.mams.user.service.IdentificationUserService;
import io.swagger.v3.oas.annotations.Operation;
import io.swagger.v3.oas.annotations.tags.Tag;
import lombok.AllArgsConstructor;
import lombok.SneakyThrows;
import lombok.extern.slf4j.Slf4j;
import org.springframework.validation.annotation.Validated;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestPart;
import org.springframework.web.bind.annotation.RestController;
import org.springframework.web.multipart.MultipartFile;


/**
 * 用户证件相关服务
 *
 * @author <a href="mailto:asialjim@hotmail.com">Asial Jim</a>
 * @version 1.0
 * @since 2025/3/13, &nbsp;&nbsp; <em>version:1.0</em>
 */
@Slf4j
@Validated
@RestController
@AllArgsConstructor
@Tag(name = "用户证件相关服务")
@RequestMapping("/identification")
public class UserIdentificationController {
    private final IdentificationUserService identificationUserService;


    @SneakyThrows
    @PostMapping("/auth")
    @Operation(summary = "证件实名")
    public IdentificationUser authenticate(@RequestPart MultipartFile file) {
        return this.identificationUserService.authenticate(file);
    }
}